public class Days {
    public static void main(String []args){
        int week = 5;
        String day;
        switch (week){
            case 1:
            day = "sunday";
            break;
            case 2:
            day = ("monday");
            break;
            case 3:
            day = ("tuesday");
            break;
            case 4:
            day = ("wednesday");
            case 5:
            day = ("thursday");
            System.out.println("The 5th day of the week is " + day );
            
             
                    
            
        }
    }
}
